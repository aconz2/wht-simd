import math

# adapted from https://en.wikipedia.org/wiki/Fast_Walsh%E2%80%93Hadamard_transform
def fwht(n):
    assert math.log2(n).is_integer()
    h = 1
    ops = [None] * n
    round_ = 0
    while h < n:
        print(f'== round {round_} == ')
        for i in range(0, n, h * 2):
            for j in range(i, i + h):
                # x = a[j]
                # y = a[j + h]
                # a[j] = x + y
                # a[j + h] = x - y
                ops[j] = f'{j:2x} + {j+h:2x}'
                ops[j + h] = f'{j:2x} - {j+h:2x}'
        for i, op in enumerate(ops):
            print(f'[{i:2x}] = {op}')
        print()
        round_ += 1
        h *= 2

